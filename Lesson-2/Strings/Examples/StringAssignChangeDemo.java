public class StringAssignChangeDemo {
    public static void main(String[] args) {
        String s1 = "Noah";
        String s2 = s1;
        System.out.println(s1);
        System.out.println(s2 + "\n");

        s1 = "Charles";
        System.out.println(s1);
        System.out.println(s2 + "\n");
    }
}
